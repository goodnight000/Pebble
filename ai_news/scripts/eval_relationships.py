def evaluate_baseline(
    pairs: List[Dict[str, Any]] | None = None,
) -> Dict[str, Any]:
    """Run the mechanical relationship pipeline on the eval set and measure recall/precision.

    Returns a dict with:
    - ``total_pairs``: number of pairs evaluated
    - ``by_label``: per-label counts and recall/precision
    - ``baseline_recall``: fraction of non-unrelated pairs that produced an edge
    - ``baseline_precision``: fraction of predicted edges that are non-unrelated
    """
    if pairs is None:
        pairs = EVAL_PAIRS

    results_by_label: Dict[str, Dict[str, int]] = {
        label: {"count": 0, "has_edge": 0}
        for label in ("follow-up", "reaction", "competing", "unrelated")
    }

    for pair in pairs:
        label = pair["label"]
        cluster_a = pair["cluster_a"]
        cluster_b = pair["cluster_b"]

        edges = compute_cluster_relationships(
            [cluster_a, cluster_b],
            embedding_threshold=0.55,
        )

        has_edge = len(edges) > 0
        results_by_label[label]["count"] += 1
        if has_edge:
            results_by_label[label]["has_edge"] += 1

    # Compute aggregate metrics
    true_positive = sum(
        results_by_label[l]["has_edge"]
        for l in ("follow-up", "reaction", "competing")
    )
    actual_positive = sum(
        results_by_label[l]["count"]
        for l in ("follow-up", "reaction", "competing")
    )
    false_positive = results_by_label["unrelated"]["has_edge"]
    total_predicted = true_positive + false_positive

    baseline_recall = true_positive / actual_positive if actual_positive > 0 else 0.0
    baseline_precision = true_positive / total_predicted if total_predicted > 0 else 0.0

    return {
        "total_pairs": len(pairs),
        "by_label": {
            label: {
                **stats,
                "recall" if label != "unrelated" else "false_positive_rate": (
                    stats["has_edge"] / stats["count"] if stats["count"] > 0 else 0.0
                ),
            }
            for label, stats in results_by_label.items()
        },
        "baseline_recall": round(baseline_recall, 4),
        "baseline_precision": round(baseline_precision, 4),
    }


def print_baseline_report(results: Dict[str, Any] | None = None) -> None:
    """Pretty-print baseline evaluation results."""
    if results is None:
        results = evaluate_baseline()

    print(f"\n{'='*60}")
    print("Relationship Inference — Mechanical Baseline")
    print(f"{'='*60}")
    print(f"Total pairs: {results['total_pairs']}")
    print(f"Baseline recall:    {results['baseline_recall']:.1%}")
    print(f"Baseline precision: {results['baseline_precision']:.1%}")
    print(f"\n{'Label':<15} {'Count':>6} {'Has Edge':>9} {'Rate':>8}")
    print(f"{'-'*40}")
    for label, stats in results["by_label"].items():
        metric_name = "recall" if label != "unrelated" else "false_positive_rate"
        rate = stats.get(metric_name, 0.0)
        print(f"{label:<15} {stats['count']:>6} {stats['has_edge']:>9} {rate:>7.1%}")
    print()
