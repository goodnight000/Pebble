def _zero_embedding_bytes(dim: int = 384) -> bytes:
    """Return a zero-vector encoded as float32 bytes (gives emb_sim = 0)."""
    return struct.pack(f"{dim}f", *([0.0] * dim))


def _make_cluster(
    *,
    cid: str,
    headline: str,
    summary: str = "",
    entities: list[dict[str, Any]] | None = None,
    dominant_event_type: str = "OTHER",
    dominant_topic: str = "mixed",
    topic_weights: dict[str, float] | None = None,
    age_hours: float = 12.0,
    coverage_count: int = 2,
) -> Dict[str, Any]:
    return {
        "id": cid,
        "headline": headline,
        "top_summary": summary,
        "centroid_embedding": _zero_embedding_bytes(),
        "entities": entities or [],
        "dominant_event_type": dominant_event_type,
        "dominant_topic": dominant_topic,
        "topic_weights": topic_weights or {},
        "age_hours": age_hours,
        "coverage_count": coverage_count,
    }


